char	*ft_strchr(const char *s, int c)
{
	if (s)
	{
		while (*s)
		{
			if (*s == (c & 255))
				return ((char *)s);
			++s;
		}
		if (!*s && *s == (c & 255))
			return ((char *)s);
	}
	return (0);
}
