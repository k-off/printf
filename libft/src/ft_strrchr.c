char	*ft_strrchr(const char *s, int c)
{
	char	*ret;

	ret = 0;
	if (s)
	{
		while (1)
		{
			if (*s == (c & 255))
				ret = (char *)s;
			if (!*s)
				break ;
			++s;
		}
	}
	return (ret);
}
